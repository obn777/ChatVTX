import datetime

def solve(expression):
    """
    Интеллектуальный модуль ГЕОЛОГИЯ.
    Анализирует запросы по минералогии, петрографии и тектонике.
    """
    text = expression.lower().strip()
    timestamp = datetime.datetime.now().strftime("%H:%M:%S")
    header = f"🤖 [ГЕОЛОГИЯ | {timestamp}]"

    # --- Твердость и минералы ---
    if any(word in text for word in ["шкала мооса", "твердость", "минерал"]):
        return (
            f"{header}: Классификация относительной твердости.\n"
            "• 1-2 (Мягкие): Тальк, Гипс (царапаются ногтем).\n"
            "• 3-6 (Средние): Кальцит, Флюорит, Апатит, Ортоклаз (нож/стекло).\n"
            "• 7-10 (Твердые): Кварц, Топаз, Корунд, Алмаз (режут стекло).\n"
            "Примечание: Алмаз в 4 раза тверже корунда."
        )

    # --- Строение и Химия Земли ---
    if any(word in text for word in ["состав земли", "ядро", "мантия", "кора"]):
        return (
            f"{header}: Глобальная структура геосфер.\n"
            "1. Литосфера: Сиаль (Si+Al) и Сима (Si+Mg).\n"
            "2. Мантия: Оливины и пироксены. Нижняя мантия достигает 2500°C.\n"
            "3. Ядро: Железо (Fe) + Никель (Ni). Внутреннее ядро под давлением 3.5 млн атм."
        )

    # --- Генезис горных пород ---
    if any(word in text for word in ["породы", "гранит", "базальт", "мрамор"]):
        return (
            f"{header}: Генетическая классификация.\n"
            "🌋 Магматические: Интрузивные (Гранит) и Эффузивные (Базальт).\n"
            "🐚 Осадочные: Обломочные (Песок), Хемогенные (Соль), Органогенные (Уголь).\n"
            "🌀 Метаморфические: Гнейс, Сланцы, Мрамор (перекристаллизация под давлением)."
        )

    # --- Тектоника и Сейсмика ---
    if any(word in text for word in ["плиты", "землетрясение", "разлом"]):
        return (
            f"{header}: Тектоническая активность.\n"
            "Границы плит делятся на: \n"
            "• Дивергентные (расхождение — рифты).\n"
            "• Конвергентные (столкновение — горы, субдукция).\n"
            "• Трансформные (сдвиг — разлом Сан-Андреас)."
        )

    # --- Дефолтный ответ ---
    return f"{header}: Модуль активен. Запрос '{text}' требует уточнения параметров (минералогия/тектоника/состав)."

# Тестовый запуск
if __name__ == "__main__":
    print(solve("Расскажи про шкалу мооса"))
