        results = []

        # Поиск по ключевым словам
        if "vast" in ql or "васт" in ql:
            results.append(self.configs["vast"])
        if "duck" in ql or "дак" in ql:
            results.append(self.configs["duck dns"])
        if "github" in ql or "копайлот" in ql:
            results.append(self.configs["github copilot"])
        if "сервер" in ql or "настройк" in ql:
            if not results: # Добавляем общую инфу, если ничего конкретного не найдено
                results.append(self.configs["server"])

        if results:
            return "СЕРВЕРНАЯ НАВИГАЦИЯ. " + " ".join(results)

        return None

