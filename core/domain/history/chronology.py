import re

class Chronology:
    """
    Интеллектуальный хронограф NovBase. 
    Синхронизирует исторические эпохи и личности.
    """
    def __init__(self):
        # Хронологические вехи
        self.milestones = {
            862: "призвание варягов и начало русской государственности.",
            988: "крещение Руси князем Владимиром.",
            1147: "первое упоминание Москвы в летописях.",
            1242: "ледовое побоище на Чудском озере.",
            1380: "куликовская битва.",
            1453: "падение Константинополя.",
            1547: "венчание Ивана Грозного на царство.",
            1703: "основание города Санкт-Петербург.",
            1812: "бородинское сражение.",
            1861: "отмена крепостного права в России.",
            1917: "великая российская революция.",
            1945: "победа над нацизмом в Великой Отечественной войне.",
            1961: "первый полет человека в космос. Юрий Гагарин.",
            1991: "распад Советского Союза."
        }

        # Антропологические данные (личности)
        self.persons = {
            "петр 1": "Пётр Первый Великий. Реформатор, создатель флота и первый император России.",
            "сталин": "Иосиф Сталин. Лидер СССР в период индустриализации и Второй мировой войны.",
            "ленин": "Владимир Ленин. Идеолог революции и основатель советского государства.",
            "екатерина 2": "Екатерина Вторая. Эпоха просвещенного абсолютизма и золотой век дворянства.",
            "иван грозный": "Иван Четвертый. Первый царь всея Руси, расширивший границы на восток.",
            "наполеон": "Наполеон Бонапарт. Французский император, изменивший карту Европы."
        }

    def _get_century(self, year: int) -> str:
        """Преобразование года в числительное века для озвучки."""
        century = (abs(year) - 1) // 100 + 1
        mapping = {
            1: "первый", 2: "второй", 3: "третий", 4: "четвертый", 5: "пятый",
            6: "шестой", 7: "седьмой", 8: "восьмой", 9: "девятый", 10: "десятый",
            11: "одиннадцатый", 12: "двенадцатый", 13: "тринадцатый", 14: "четырнадцатый",
            15: "пятнадцатый", 16: "шестнадцатый", 17: "семнадцатый", 18: "восемнадцатый",
            19: "девятнадцатый", 20: "двадцатый", 21: "двадцать первый"
        }
        return mapping.get(century, str(century)) + " век"

    def solve(self, expression: str):
        """Анализ временных меток и имен."""
        text = expression.lower()
        results = []

        # 1. Поиск исторических фигур
        for name, bio in self.persons.items():
            if name in text:
                results.append(f"Личность: {bio}")

        # 2. Поиск дат (от 3 до 4 цифр)
        years = re.findall(r'\b(\d{3,4})\b', text)
        for y_str in years:
            year = int(y_str)
            if 1 <= year <= 2026:
                century = self._get_century(year)
                info = f"В {year} году, а это {century}, "
                if year in self.milestones:
                    info += f"произошло ключевое событие: {self.milestones[year]}"
                else:
                    info += "значимых совпадений в базе хронологии не обнаружено."
                results.append(info)

        if results:
            return "ИСТОРИЧЕСКИЙ КОНТЕКСТ. " + " ".join(results)
        return None

# Инициализация модуля
chrono = Chronology()

def solve(expression):
    return chrono.solve(expression)
