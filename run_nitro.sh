#!/bin/bash

# ==================================================
# Конфигурация путей
# ==================================================
BASE_DIR="/home/obn7/NovBase"
PYTHON_APP="$BASE_DIR/app.py"
LOG_DIR="$BASE_DIR/storage"
CADDYFILE="$BASE_DIR/Caddyfile"
APP_LOG="$LOG_DIR/app.log"

# Создаем папку логов, если её нет
mkdir -p $LOG_DIR

echo "------------------------------------------------"
echo "🚀 Инициализация узла NovBase на Nitro-ANV15-41"
echo "------------------------------------------------"

# ==================================================
# 1. ПОЛНАЯ ОЧИСТКА ПЕРЕД СТАРТОМ
# ==================================================
echo "🧹 Очистка старых процессов и портов..."
pkill -9 -f "python3 $PYTHON_APP"
pkill -9 -f "bot.py"
pkill -9 -f "caddy"
fuser -k 8080/tcp > /dev/null 2>&1
sleep 2

# Очищаем лог-файл (режим "только последний запуск")
echo "[$(date '+%Y-%m-%d %H:%M:%S')] --- СИСТЕМА ИНИЦИАЛИЗИРОВАНА ---" > "$APP_LOG"

# ==================================================
# 2. ВИРТУАЛЬНОЕ ОКРУЖЕНИЕ
# ==================================================
if [ -d "$BASE_DIR/venv" ]; then
    echo "📦 Активация виртуального окружения..."
    source "$BASE_DIR/venv/bin/activate"
fi

# ==================================================
# 3. ЗАПУСК CADDY
# ==================================================
if [ -f "$CADDYFILE" ]; then
    echo "🌐 Запуск Caddy..."
    caddy start --config "$CADDYFILE" >> "$LOG_DIR/caddy.log" 2>&1
    echo "✅ Caddy активен."
fi

# ==================================================
# 4. СТАТУС ВИДЕОКАРТЫ
# ==================================================
echo "📊 Статус видеокарты Nitro RTX 3050:"
nvidia-smi --query-gpu=name,memory.total,memory.free --format=csv,noheader | sed 's/^/    /'

# ==================================================
# 5. ЗАПУСК ЕДИНОГО ЯДРА (FLASK + BOT)
# ==================================================
echo "🧬 Запуск Единого Ядра (Web + Telegram)..."
echo "📝 Логи пишутся в $APP_LOG"
echo "------------------------------------------------"

# Бесконечный цикл с дозаписью в уже очищенный файл
while true; do
    python3 "$PYTHON_APP" >> "$APP_LOG" 2>&1

    # Если процесс завершился (ошибка или краш)
    EXIT_CODE=$?
    echo "[$(date '+%H:%M:%S')] 🚨 СБОЙ (Code: $EXIT_CODE). Перезапуск через 5 сек..." >> "$APP_LOG"
    echo "⚠️ Система упала. Проверь лог. Перезагрузка..."

    # Очистка портов перед повторным входом в цикл
    fuser -k 8080/tcp > /dev/null 2>&1
    sleep 5
done
